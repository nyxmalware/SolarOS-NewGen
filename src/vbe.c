void vbe_init(uint32_t addr,uint32_t pitch,uint32_t width,uint32_t height,uint32_t bpp){
fb_addr=(uint32_t*)(uint64_t)addr;
fb_pitch=pitch;
fb_width=width;
fb_height=height;
fb_bpp=bpp;
}
void vbe_put_pixel(uint32_t x,uint32_t y,uint32_t color){
if(!fb_addr||x>=fb_width||y>=fb_height) return;
uint32_t *pixel=(uint32_t*)((uint8_t*)fb_addr+y*fb_pitch+x*(fb_bpp/8));
*pixel=color;
}
void vbe_fill_rect(uint32_t x,uint32_t y,uint32_t w,uint32_t h,uint32_t color){
if(!fb_addr) return;
for(uint32_t j=y;j<y+h&&j<fb_height;j++){
for(uint32_t i=x;i<x+w&&i<fb_width;i++){
uint32_t *pixel=(uint32_t*)((uint8_t*)fb_addr+j*fb_pitch+i*(fb_bpp/8));
*pixel=color;
}
}
}
void vbe_clear(uint32_t color){
if(!fb_addr) return;
vbe_fill_rect(0,0,fb_width,fb_height,color);
}
void vbe_draw_char(uint32_t x,uint32_t y,char c,uint32_t fg,uint32_t bg){
if(c<0||c>=128) return;
for(int row=0;row<8;row++){
uint8_t bits=font8x8[(uint8_t)c][row];
for(int col=0;col<8;col++){
uint32_t color=(bits&(0x80>>col))?fg:bg;
vbe_put_pixel(x+col,y+row,color);
}
}
}
void vbe_draw_string(uint32_t x,uint32_t y,const char *s,uint32_t fg,uint32_t bg){
while(*s){
vbe_draw_char(x,y,*s,fg,bg);
x+=8;
s++;
}
}
