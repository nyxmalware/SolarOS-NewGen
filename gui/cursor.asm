; курсор мыши

cursor_visible db 1
cursor_x dw 0
cursor_y dw 0
cursor_saved dw 16 dup(0)

mouse_show:
    mov byte [cursor_visible], 1
    call draw_cursor
    ret

mouse_hide:
    mov byte [cursor_visible], 0
    call restore_cursor
    ret

draw_cursor:
    pusha
    cmp byte [cursor_visible], 0
    je .done
    
    mov ax, [cursor_x]
    mov bx, [cursor_y]
    
    ; сохранение под курсором
    mov cx, 16
.save:
    push cx
    push ax
    push bx
    call get_pixel
    mov si, cursor_saved
    add si, cx
    mov [si], al
    pop bx
    pop ax
    inc ax
    pop cx
    loop .save
    
    ; отрисовка курсора
    mov si, cursor_data
    mov cx, 16
.row:
    push cx
    mov cx, 16
    mov dl, [si]
.col:
    test dl, 0x80
    jz .skip
    pusha
    mov bl, 0x0F
    call put_pixel
    popa
.skip:
    shl dl, 1
    inc ax
    loop .col
    inc bx
    pop cx
    inc si
    loop .row
    
.done:
    popa
    ret

restore_cursor:
    pusha
    mov ax, [cursor_x]
    mov bx, [cursor_y]
    mov cx, 16
    mov si, cursor_saved
.restore:
    push cx
    push ax
    push bx
    mov al, [si]
    call put_pixel
    pop bx
    pop ax
    inc ax
    pop cx
    inc si
    loop .restore
    popa
    ret

update_cursor:
    pusha
    call mouse_hide
    mov ax, [mouse_x]
    mov [cursor_x], ax
    mov ax, [mouse_y]
    mov [cursor_y], ax
    call mouse_show
    popa
    ret

get_pixel:
    pusha
    mov cx, 640
    mul cx
    add ax, bx
    mov si, ax
    mov al, [0xA0000 + si]
    mov [.pixel], al
    popa
    mov al, [.pixel]
    ret
.pixel: db 0

